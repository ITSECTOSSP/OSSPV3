export const getPillColor = (id: string | number) => {
    const str = String(id);

    let hash = 0;
    for (let i = 0; i < str.length; i++) {
        hash = (hash << 5) - hash + str.charCodeAt(i);
        hash |= 0;
    }

    return pillColors[Math.abs(hash) % pillColors.length];
};